-- 验证查询
SELECT 
  '2024年中药学综合知识与技能真题' as 描述,
  COUNT(*) as 总数,
  COUNT(*) FILTER (WHERE question_type = 'single') as 单选题,
  COUNT(*) FILTER (WHERE question_type = 'multiple') as 多选题,
  COUNT(*) FILTER (WHERE difficulty = 2) as 中等难度,
  COUNT(*) FILTER (WHERE difficulty = 3) as 较难
FROM questions
WHERE source_year = 2024 AND subject = '中药学综合知识与技能';

-- 查看部分题目
SELECT 
  id,
  LEFT(content, 30) || '...' as 题目简要,
  correct_answer as 答案,
  difficulty as 难度,
  knowledge_points[1] as 知识点1
FROM questions
WHERE source_year = 2024 AND subject = '中药学综合知识与技能'
ORDER BY id
LIMIT 10;
